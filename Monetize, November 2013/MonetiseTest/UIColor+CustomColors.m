#import "UIColor+CustomColors.h"

@implementation UIColor (CustomColors)

+(UIColor *)maleColor
{
    return [UIColor colorWithRed:0.9f green:0.9f blue:1.0f alpha:1.0f];
};

+(UIColor *)femaleColor
{
    return [UIColor colorWithRed:1.0f green:0.9f blue:0.9f alpha:1.0f];
}

@end
